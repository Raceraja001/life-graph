@dataclass
class AskResult:
    """Result of an ``/ask`` query.

    Attributes:
        answer: Natural-language answer generated by the API.
        source_count: Number of source memories used to generate the answer.
        model: The model used for answer generation, if reported.
        memories: Backing memories used for the answer.
        query_time_ms: Time taken to process the query in milliseconds.
    """

    answer: str
    source_count: int = 0
    model: str | None = None
    memories: list[Memory] = field(default_factory=list)
    query_time_ms: float = 0.0

    @classmethod
    def from_dict(cls, data: dict) -> AskResult:
        """Create an AskResult from an API response dict.

        Args:
            data: A dict decoded from the API JSON payload.

        Returns:
            An :class:`AskResult` instance.
        """
        raw_memories = data.get("memories") or []
        return cls(
            answer=data.get("answer", ""),
            source_count=int(data.get("source_count", 0)),
            model=data.get("model"),
            memories=[Memory.from_dict(m) for m in raw_memories],
            query_time_ms=float(data.get("query_time_ms", 0.0)),
        )


