@dataclass
class ApiResponse:
    """Generic wrapper matching the API's ``{"data": ..., "meta": {...}}`` envelope.

    Attributes:
        data: The unwrapped payload (any type).
        meta: Optional metadata dict returned alongside the payload.
    """

    data: Any = None
    meta: dict | None = None

    @classmethod
    def from_dict(cls, data: dict) -> ApiResponse:
        """Create an ApiResponse from a raw JSON dict.

        Args:
            data: The raw JSON dict from the API.

        Returns:
            An :class:`ApiResponse` instance.
        """
        return cls(
            data=data.get("data"),
            meta=data.get("meta"),
        )
