"""Data types for the Life Graph SDK.

Every public dataclass corresponds to a resource returned by the Life Graph
v1 API and includes a ``from_dict`` classmethod for convenient
deserialisation from JSON payloads.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any


# ---------------------------------------------------------------------------
# Core memory types
# ---------------------------------------------------------------------------

@dataclass
class Memory:
    """A single memory stored in Life Graph.

    Attributes:
        id: Unique memory identifier.
        content: The textual content of the memory.
        tags: Classification tags attached to the memory.
        properties: Arbitrary key-value metadata.
        importance: Numeric importance score (0.0 – 1.0).
        confidence: Numeric confidence score (0.0 – 1.0).
        reasoning: Optional reasoning explanation for the memory.
        source_type: Origin of the memory (e.g. ``"ingest"``, ``"voice"``).
        status: Lifecycle status (e.g. ``"active"``, ``"superseded"``).
        created_at: ISO-8601 creation timestamp.
        updated_at: ISO-8601 last-update timestamp.
        access_count: Number of times this memory has been accessed.
    """

    id: str
    content: str
    tags: list[str] = field(default_factory=list)
    properties: dict = field(default_factory=dict)
    importance: float = 0.0
    confidence: float = 0.0
    reasoning: str | None = None
    source_type: str | None = None
    status: str | None = None
    created_at: str | None = None
    updated_at: str | None = None
    access_count: int = 0
    impact_score: float = 0.5

    @classmethod
    def from_dict(cls, data: dict) -> Memory:
        """Create a Memory from an API response dict.

        Args:
            data: A dict decoded from the API JSON payload.

        Returns:
            A fully-populated :class:`Memory` instance.
        """
        return cls(
            id=str(data.get("id", "")),
            content=data.get("content", ""),
            tags=data.get("tags", []),
            properties=data.get("properties", {}),
            importance=float(data.get("importance", 0.0)),
            confidence=float(data.get("confidence", 0.0)),
            reasoning=data.get("reasoning"),
            source_type=data.get("source_type"),
            status=data.get("status"),
            created_at=data.get("created_at"),
            updated_at=data.get("updated_at"),
            access_count=int(data.get("access_count", 0)),
            impact_score=float(data.get("impact_score", 0.5)),
        )


